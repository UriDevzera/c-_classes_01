// import biblioteca de entrada e saída
#include <iostream>

// usando o namespace std
using namespace std;

int main()
{
    int x{3}, y{6};

    cout << "x = " << x << " e y = " << y << '\n';
    cout << "soma x + y = " << x + y << '\n';
    cout << "subtrai x - y = " << x - y << '\n';
    cout << "multiplica x * y = " << x * y << '\n';
    cout << "divide x / y = " << x / y << '\n';
    cout << "resto x % y = " << x % y << '\n';

    return 0;
}

